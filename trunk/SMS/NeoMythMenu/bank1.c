void bank1_load_bg()
{
    WORD i;

    // The font patterns occupy the first 120 tiles, so load the
    // bg patterns directly following that
    pfn_vdp_set_vram_addr(0x0F00); // after font - 120 tiles * 32
    for (i = 0; i < 888; i+=3)
    {
        VdpData = menu_pat[i];
        VdpData = menu_pat[i+1];
        VdpData = menu_pat[i+2];
        // Bitplane 3 is empty
        VdpData = 0;
    }

    // Load the nametabel data
    pfn_vdp_set_vram_addr(0x1800);
    for (i = 0; i < 768; i++)
    {
        VdpData = menu_nam[i+i];
        VdpData = 0;
    }

    // Load the palette
    pfn_vdp_copy_to_cram(0, menu_pal, 8);
}
